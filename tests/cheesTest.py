# Импортируем функцию, которую будем тестировать
from modules.chees import *

"""
   Тестирует функцию isSameColor.

   Проверяет различные случаи:
   1. Поля одного цвета (суммы координат четные или нечетные).
   2. Поля разного цвета (одна сумма четная, другая нечетная).
   3. Граничные случаи (минимальные и максимальные значения координат).
   """
def testIsSameColor():
    # Тест 1: Поля одного цвета (обе суммы четные)
    assert isSameColor(1, 1, 2, 2) == True, "Ошибка в тесте 1: поля (1,1) и (2,2) должны быть одного цвета"

    # Тест 2: Поля разного цвета (одна сумма четная, другая нечетная)
    assert isSameColor(1, 1, 1, 2) == False, "Ошибка в тесте 3: поля (1,1) и (1,2) должны быть разного цвета"

    # Тест 3: Граничный случай (минимальные значения координат)
    assert isSameColor(1, 1, 1, 1) == True, "Ошибка в тесте 5: поля (1,1) и (1,1) должны быть одного цвета"

    # Тест 4: Граничный случай (максимальные значения координат)
    assert isSameColor(8, 8, 8, 8) == True, "Ошибка в тесте 6: поля (8,8) и (8,8) должны быть одного цвета"

    # Тест 5: Поля одного цвета (случайные координаты)
    assert isSameColor(3, 5, 4, 6) == True, "Ошибка в тесте 7: поля (3,5) и (4,6) должны быть одного цвета"

    # Тест 6: Исключение
    try:
        isSameColor(0, 0, 0, 0)
        assert False, "Ошибка в тесте 6: ожидалось исключение ValueError"
    except ValueError:
        pass  # Исключение успешно поймано

    return True
