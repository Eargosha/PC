''' 
76a. Поле шахматной доски определяется парой натуральных чисел, каждое 
из которых не превосходит восьми: первое число — номер вертикали 
(при счете слева направо), второе — номер горизонтали (при счете снизу вверх). 
Даны натуральные числа k, l, m, n, каждое из которых не превосходит восьми. Требуется:
а) Выяснить, являются ли поля (k, l) и (m, n) полями одного цвета.
'''

"""
Определяет, являются ли поля (k, l) и (m, n) одного цвета на шахматной доске.

:param k: Номер вертикали первого поля (1-8).
:param l: Номер горизонтали первого поля (1-8).
:param m: Номер вертикали второго поля (1-8).
:param n: Номер горизонтали второго поля (1-8).
:return: True, если поля одного цвета, иначе False.
"""
def isSameColor(k, l, m, n):

    # Проверяем диапазон
    if not (validateInput(k) and validateInput(l) and validateInput(m) and validateInput(n)):
        raise ValueError("Все числа должны быть от 1 до 8.")

    # Сумма координат первого поля
    sum1 = k + l
    # Сумма координат второго поля
    sum2 = m + n
    # Если обе суммы четные или обе нечетные, то поля одного цвета
    return (sum1 % 2) == (sum2 % 2)

"""
Проверяет ввод числа от 1 до 8.
:param prompt: Подсказка для ввода.
:return: True/False от результата
"""
def validateInput(prompt):
    if 1 <= prompt <= 8:
        return True
    else:
        return False